-- CRUD en tablas de relación de contenido
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.CONTENIDO_GENERO    TO ROL_CONTENIDO;
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.CONTENIDO_RELACIONADO TO ROL_CONTENIDO;

-- SELECT en reproducciones (para ver estadísticas de consumo)

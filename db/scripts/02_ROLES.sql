GRANT SELECT ON C_QUINDIOFLIX.REFERIDO             TO ROL_SOPORTE;
GRANT SELECT ON C_QUINDIOFLIX.CIUDAD             TO ROL_SOPORTE;

-- INSERT/UPDATE en PAGOS (para registrar pagos manuales, ajustes, reembolsos)
GRANT INSERT, UPDATE ON C_QUINDIOFLIX.PAGO          TO ROL_SOPORTE;

-- Ejecutar procedimiento para cambiar plan de suscripción
BEGIN
    EXECUTE IMMEDIATE 'GRANT EXECUTE ON C_QUINDIOFLIX.SP_CAMBIAR_PLAN TO ROL_SOPORTE';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/


-- ============================================================
-- 4. ROL_CONTENIDO
--    Gestor de catálogo
-- ============================================================
CREATE ROLE ROL_CONTENIDO;

-- CRUD completo en tablas de contenido
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.CONTENIDO           TO ROL_CONTENIDO;
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.TEMPORADA          TO ROL_CONTENIDO;

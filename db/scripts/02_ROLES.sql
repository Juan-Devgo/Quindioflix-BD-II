-- ============================================================
-- 1. ROL_ADMIN
--    Administrador de la plataforma
-- ============================================================
CREATE ROLE ROL_ADMIN;

-- CRUD completo en todas las tablas del esquema C_QUINDIOFLIX
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.PLAN_SUSCRIPCION    TO ROL_ADMIN;
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.CIUDAD             TO ROL_ADMIN;
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.USUARIO             TO ROL_ADMIN;
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.REFERIDO             TO ROL_ADMIN;
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.PERFIL               TO ROL_ADMIN;
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.DEPARTAMENTO        TO ROL_ADMIN;
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.EMPLEADO            TO ROL_ADMIN;
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.GENERO              TO ROL_ADMIN;
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.CONTENIDO           TO ROL_ADMIN;
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.CONTENIDO_GENERO    TO ROL_ADMIN;
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.CONTENIDO_RELACIONADO TO ROL_ADMIN;
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.TEMPORADA          TO ROL_ADMIN;
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.EPISODIO            TO ROL_ADMIN;
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.REPRODUCCION        TO ROL_ADMIN;
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.FAVORITO            TO ROL_ADMIN;
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.CALIFICACION        TO ROL_ADMIN;
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.REPORTE_CONTENIDO   TO ROL_ADMIN;
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.PAGO                TO ROL_ADMIN;
GRANT SELECT, INSERT, UPDATE, DELETE ON C_QUINDIOFLIX.RESTRICCION_DOMINIO TO ROL_ADMIN;

-- Ejecutar todos los procedimientos, funciones y triggers
GRANT EXECUTE ANY PROCEDURE TO ROL_ADMIN;

-- Crear/eliminar usuarios (administración de cuentas de la plataforma)
GRANT CREATE USER TO ROL_ADMIN;
GRANT DROP USER TO ROL_ADMIN;
GRANT ALTER USER TO ROL_ADMIN;

-- Gestión de roles (para asignar roles de la plataforma)
GRANT ROL_ANALISTA TO ROL_ADMIN WITH ADMIN OPTION;
GRANT ROL_SOPORTE  TO ROL_ADMIN WITH ADMIN OPTION;
GRANT ROL_CONTENIDO TO ROL_ADMIN WITH ADMIN OPTION;



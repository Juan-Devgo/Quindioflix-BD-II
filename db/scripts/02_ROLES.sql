-- ============================================================
-- 3. ROL_SOPORTE
--    Soporte al cliente
-- ============================================================
CREATE ROLE ROL_SOPORTE;

-- SELECT en tablas de usuarios, perfiles, pagos y suscripciones
GRANT SELECT ON C_QUINDIOFLIX.USUARIO             TO ROL_SOPORTE;
GRANT SELECT ON C_QUINDIOFLIX.PERFIL               TO ROL_SOPORTE;
GRANT SELECT ON C_QUINDIOFLIX.PAGO                TO ROL_SOPORTE;

-- ROL_SOPORTE: Soporte al cliente
CREATE ROLE ROL_SOPORTE;

BEGIN
    FOR t IN (
        SELECT 'USUARIO' AS nm, 'SELECT' AS priv FROM DUAL UNION ALL
        SELECT 'PERFIL', 'SELECT' FROM DUAL UNION ALL
        SELECT 'PAGO', 'SELECT' FROM DUAL UNION ALL
        SELECT 'PAGO', 'INSERT, UPDATE' FROM DUAL UNION ALL
        SELECT 'PLAN_SUSCRIPCION', 'SELECT' FROM DUAL UNION ALL
        SELECT 'REFERIDO', 'SELECT' FROM DUAL UNION ALL
        SELECT 'CIUDAD', 'SELECT' FROM DUAL
    ) LOOP
        BEGIN
            EXECUTE IMMEDIATE 'GRANT ' || t.priv || ' ON ' || t.nm || ' TO ROL_SOPORTE';
        EXCEPTION WHEN OTHERS THEN NULL;
        END;
    END LOOP;
END;

BEGIN
    EXECUTE IMMEDIATE 'GRANT EXECUTE ON SP_CAMBIAR_PLAN TO ROL_SOPORTE';
EXCEPTION WHEN OTHERS THEN NULL;
END;



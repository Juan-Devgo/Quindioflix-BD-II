-- ROL_ADMIN: Administrador de la plataforma
CREATE ROLE ROL_ADMIN;

BEGIN
    FOR t IN (
        SELECT 'PLAN_SUSCRIPCION' AS nm FROM DUAL UNION ALL
        SELECT 'CIUDAD' FROM DUAL UNION ALL
        SELECT 'USUARIO' FROM DUAL UNION ALL
        SELECT 'REFERIDO' FROM DUAL UNION ALL
        SELECT 'PERFIL' FROM DUAL UNION ALL
        SELECT 'DEPARTAMENTO' FROM DUAL UNION ALL
        SELECT 'EMPLEADO' FROM DUAL UNION ALL
        SELECT 'GENERO' FROM DUAL UNION ALL
        SELECT 'CONTENIDO' FROM DUAL UNION ALL
        SELECT 'CONTENIDO_GENERO' FROM DUAL UNION ALL
        SELECT 'CONTENIDO_RELACIONADO' FROM DUAL UNION ALL
        SELECT 'TEMPORADA' FROM DUAL UNION ALL
        SELECT 'EPISODIO' FROM DUAL UNION ALL
        SELECT 'REPRODUCCION' FROM DUAL UNION ALL
        SELECT 'FAVORITO' FROM DUAL UNION ALL
        SELECT 'CALIFICACION' FROM DUAL UNION ALL
        SELECT 'REPORTE_CONTENIDO' FROM DUAL UNION ALL
        SELECT 'PAGO' FROM DUAL UNION ALL
        SELECT 'RESTRICCION_DOMINIO' FROM DUAL
    ) LOOP
        BEGIN
            EXECUTE IMMEDIATE 'GRANT SELECT, INSERT, UPDATE, DELETE ON ' || t.nm || ' TO ROL_ADMIN';
        EXCEPTION WHEN OTHERS THEN NULL;
        END;
    END LOOP;
END;

GRANT EXECUTE ANY PROCEDURE TO ROL_ADMIN;
GRANT CREATE USER TO ROL_ADMIN;
GRANT DROP USER TO ROL_ADMIN;
GRANT ALTER USER TO ROL_ADMIN;
GRANT ROL_ANALISTA TO ROL_ADMIN WITH ADMIN OPTION;
GRANT ROL_SOPORTE  TO ROL_ADMIN WITH ADMIN OPTION;
GRANT ROL_CONTENIDO TO ROL_ADMIN WITH ADMIN OPTION;


